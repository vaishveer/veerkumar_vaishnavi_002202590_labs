    private void btnupdatedescriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdatedescriptionActionPerformed
        // TODO add your handling code here:
       if (!txtdescription.isEnabled()) {
            txtdescription.setEnabled(true);
        } 
       else {
            String description = txtdescription.getText().trim(); 

            if (description.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter new description to update");
            } 
            else if (description.matches(".*[^a-zA-Z\\s].*")) {
                JOptionPane.showMessageDialog(this, "Description can only contain letters");
            } 
            else {
                supplier.getProfile().setDescription(description);
                JOptionPane.showMessageDialog(this, "DESCRIPTION UPDATED SUCCESSFULLY");
                txtdescription.setEnabled(false); 
            }
        }
    }//GEN-LAST:event_btnupdatedescriptionActionPerformed

