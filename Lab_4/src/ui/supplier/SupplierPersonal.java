    private void btnupdatecontactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdatecontactActionPerformed
        // TODO add your handling code here:
        if (!txtcontact.isEnabled()) {
            txtcontact.setEnabled(true);
        }   
        else {
            String contact = txtcontact.getText().trim(); 

            if (contact.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter new contact information to update");
            } 
            else if (!contact.matches("^[0-9+]+$")) {
                JOptionPane.showMessageDialog(this, "Contact format is incorrect; it can only contain numbers and the plus sign (+)");
            } 
            else {
                long contactinfo = Long.parseLong(contact);
                supplier.getProfile().setContactInformation(contactinfo);
                JOptionPane.showMessageDialog(this, "CONTACT INFORMATION UPDATED SUCCESSFULLY");
                txtcontact.setEnabled(false); 
            }
        }
    }//GEN-LAST:event_btnupdatecontactActionPerformed

